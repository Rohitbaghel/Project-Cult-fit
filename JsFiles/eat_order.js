function showProducts() {
    let data_div = document.getElementById("mymeal");

    myproduct.forEach(function(product) {
        let div = document.createElement("div");

        let p_name = document.createElement("p");
        p_name.innerText = product.name;
        let price_div = document.createElement("span");
        price_div.setAttribute("class", "price_and_btn")

        let O_price = document.createElement("span");
        O_price.innerText = "₹" + product.OriginalPrice;

        let p_price = document.createElement("span");
        p_price.innerText = "₹" + product.price;



        let img_div=document.createElement("div");
        img_div.setAttribute("class","food_img")
        let image = document.createElement("img");
        image.src = product.image;

        let btn = document.createElement("button");
        btn.innerText = "Add";

        div.onclick = function() {
            Clicked_Product(product);
        };
        btn.onclick = function() {
            addtocart(product);
        };
        price_div.append( p_price,O_price,btn)
        img_div.appendChild(image)
        div.append(img_div, p_name, price_div);

        data_div.appendChild(div);

    });
}

